def game_over(won: bool, reason: str):
    """
    Handle game ending and offer replay option.

    Args:
        won (bool): Whether the player won or lost
        reason (str): Description of how the game ended

    Purpose:
        - Display appropriate ending message
        - Update game statistics
        - Offer replay option

    Flow:
        1. Show win/loss message
        2. Update statistics
        3. Display current path taken
        4. Offer to replay or quit
    """
    print_separator("=")

    if won:
        # Victory message
        print_slow("\n🎉 CONGRATULATIONS! 🎉")
        print_separator("-")
        print_slow(f"\n✅ {current_player_name}, you have succeeded!")
        print_slow(f"💎 {reason}")
        print_slow("\nYour name will be remembered in legends!")

        # Update win statistics
        game_stats['wins'] += 1
        game_stats['win_paths'].append(game_stats['paths_taken'].copy())

    else:
        # Loss message
        print_slow("\n💀 GAME OVER 💀")
        print_separator("-")
        print_slow(f"\n❌ {current_player_name}, your adventure has ended.")
        print_slow(f"⚠️  {reason}")
        print_slow("\nBut every failure is a lesson learned...")

        # Update loss statistics
        game_stats['losses'] += 1
        game_stats['loss_paths'].append(game_stats['paths_taken'].copy())

    # Show path taken this game
    print_separator("-")
    print("\n📍 YOUR JOURNEY:")
    for i, step in enumerate(game_stats['paths_taken'], 1):
        print(f"   {i}. {step}")

    # Clear path for next game
    game_stats['paths_taken'] = []

    time.sleep(2)

    # Offer replay
    print_separator("=")
    print("\n🔄 Would you like to play again?")
    print("1. Yes - Start a new adventure")
    print("2. No - View statistics and exit")

    replay_choice = get_player_choice([1, 2])

    if replay_choice == 1:
        # Restart the game
        print_slow("\n🔄 Starting new adventure...")
        time.sleep(1)
        start_game()
    else:
        # Show statistics and exit
        print_slow("\n👋 Thank you for playing!")
        time.sleep(0.5)
        display_game_stats()
        print_separator("=")
        print_slow("\nMay your future adventures be legendary! ⚔️")
        print_separator("=")
        sys.exit(0)

