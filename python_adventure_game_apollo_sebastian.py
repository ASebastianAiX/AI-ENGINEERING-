def clear_screen():
    """
    Clear the terminal screen for better readability.

    Purpose: Provides a clean interface between game sections
    Cross-platform: Works on Windows, Mac, and Linux
    """
    os.system('cls' if os.name == 'nt' else 'clear')


def print_slow(text: str, delay: float = 0.03):
    """
    Print text with a typewriter effect for dramatic impact.

    Args:
        text (str): The text to print
        delay (float): Delay between each character (default 0.03 seconds)

    Purpose: Creates an immersive storytelling experience
    GitHub Copilot Contribution: Suggested the character-by-character approach
    """
    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(delay)
    print()  # New line after text


def print_separator(char: str = "=", length: int = 70):
    """
    Print a decorative separator line.

    Args:
        char (str): Character to use for separator
        length (int): Length of the separator line

    Purpose: Visual organization of game sections
    """
    print(char * length)


def get_player_choice(options: List[str]) -> int:
    """
    Get and validate player input from a list of options.

    Args:
        options (List[str]): List of available choices

    Returns:
        int: The validated choice (1-based index)

    Purpose: Robust input handling with error checking
    GitHub Copilot Contribution: Suggested try-except error handling

    Example:
        options = ["Go left", "Go right"]
        choice = get_player_choice(options)
    """
    while True:
        try:
            choice = int(input("\nEnter your choice (number): "))
            if 1 <= choice <= len(options):
                game_stats['total_choices'] += 1
                return choice
            else:
                print(f"❌ Please enter a number between 1 and {len(options)}")
        except ValueError:
            print("❌ Invalid input! Please enter a number.")


def track_path(location: str, choice: str):
    """
    Track the player's journey through the game.

    Args:
        location (str): Current location in the game
        choice (str): Choice made by the player

    Purpose: Analytics for game balance and player behavior analysis
    """
    path_entry = f"{location}: {choice}"
    game_stats['paths_taken'].append(path_entry)


